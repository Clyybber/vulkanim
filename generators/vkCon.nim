import macros,vulkan,strutils,sequtils

func removeSpaces(x: string): string=
    result = x
    result.removePrefix(' ')
    result.removeSuffix(' ')

macro mkCon(T: untyped,x: static[string]):untyped=
    let procName = ident("mk" & T.strVal())
    let sType = ident("vkStructureType" & (T.strVal())[2 .. T.strVal.high])
    var args = splitLines(x).filterIt(not it.contains("object")).mapIt(it.split(":").map(removeSpaces)).filterIt(it.len() == 2)
    var setupStmlList = newStmtList()
    var inx = nnkFormalParams.newTree(T)
    for i in args:
        let tp = ident(i[0])
        var tn = parseExpr(i[1])
        if i[0] == "sType":
            setupStmlList.add quote do:
                result.sType = `sType`
        elif not(i[0].contains("flag") or i[1] == "pointer"):
            setupStmlList.add quote do:
                result.`tp` = `tp`
            inx.add nnkIdentDefs.newTree(tp,tn,newEmptyNode())
    result = quote do:
        proc `procName`*()= `setupStmlList`
    result[3] = inx
    writeFile("src/vkCon.nim",readFile("src/vkCon.nim") & "\n" & result.repr & "\n")

